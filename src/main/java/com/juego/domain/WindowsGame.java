    private void refrescar(){
        this.labelIntentos.setText("Te quedan " + num.getIntentos() + " intentos");
        if(num.esFinJuego()){
            this.btnJugar.setEnabled(false);
            this.btnReiniciar.setEnabled(true);
        }
        else {
            this.btnJugar.setEnabled(true);
            this.btnReiniciar.setEnabled(false);
        }
        this.labelIntentos.setText("");
        this.labelFinJuego.setText("");
        this.textFieldNum.setText("*");
    }
    
