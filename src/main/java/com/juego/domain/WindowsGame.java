    private void btnJugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJugarActionPerformed
        // TODO add your handling code here:
        boolean value;
        String cadena;
        String num_add = textFieldNum.getText();
        int casting = Integer.parseInt(num_add);
        value = this.num.comprobar(casting); //true o false
        cadena = this.num.ValueComprobar(value);
        this.labelIntentos.setText(this.num.AvisoIntentos());
        if(this.num.getIntentos() < 0) {
            this.num.reiniciar();
        }
        if(this.num.esFinJuego() == true){
            this.labelFinJuego.setText("Fin del Juego. " + cadena);           
        }
    }//GEN-LAST:event_btnJugarActionPerformed

