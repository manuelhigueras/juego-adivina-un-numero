    private void btnIntentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIntentoActionPerformed
        // TODO add your handling code here:
        String aviso, aviso2;
        try{
            String num_agregado = textFieldNum.getText();
            int casting = Integer.parseInt(num_agregado);
            aviso = num.CompruebaNum(this.num.getNum(), casting);
            aviso2 = num.decrementoDeIntentos();
            this.labelMensaje.setText(aviso);
            this.labelIntento.setText(aviso2);
            this.labelAviso.setText(num.EstadoJuego());
        }
        catch(NumberFormatException e){
            aviso = "Este valor no es valido - Again";
            this.labelMensaje.setText(aviso);
        }
    }//GEN-LAST:event_btnIntentoActionPerformed

